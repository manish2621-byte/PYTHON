''' Write a Python program to import the math module and use functions like sqrt(), ceil(),
floor().'''
# Import math module
import math

num = 25
value = 7.8

print("Square Root:", math.sqrt(num))
print("Ceiling Value:", math.ceil(value))
print("Floor Value:", math.floor(value))


