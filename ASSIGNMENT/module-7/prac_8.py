'''Write
a Python program to create a parameterized function that takes two arguments and prints their
sum.'''

# Parameterized function to print the sum of two numbers

def add(a, b):
    print("Sum =", a + b)

add(10, 20)


'''Write a Python program to create a lambda function with one expression'''
# Lambda function with one expression

square = lambda x: x * x

print(square(5))

'''Write
a Python program to create a lambda function with two expressions'''
# Lambda function with two arguments

add = lambda a, b: a + b

print(add(10, 20))

